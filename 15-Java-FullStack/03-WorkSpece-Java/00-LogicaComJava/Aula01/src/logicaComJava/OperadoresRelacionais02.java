package logicaComJava;

public class OperadoresRelacionais02 {

	public static void main(String[] args) {
		
		int num1 = 10;
		int num2 = 10;
		
		if(num1 == num2) {
			System.out.println("Números são iguais");
		}else if(num1 != num2){
			System.out.println("Ganhou mais ");
		
		}else {
			System.out.println("Nada");
		}
		
	}

}
