package figuras_geometricas;

public class FigurasGeometricas {

	public static void main(String[] args) {
		// Arrow
	    System.out.println("    *");
	    System.out.println("   ***");
	    System.out.println("  *****");
	    System.out.println(" *******");
	    System.out.println("*********");
	    System.out.println("     *   ");
	    System.out.println("     *   ");
	    System.out.println("     *   ");
	    System.out.println("     *   \n");
	    
	    // Box
	    System.out.println(" *****");
	    System.out.println(" *   *");
	    System.out.println(" *   *");
	    System.out.println(" *****\n");
	    
	    // Diamond
	    System.out.println("    *");
	    System.out.println("   *  *");
	    System.out.println("  *     *");
	    System.out.println(" *       *");
	    System.out.println("*         *");
	    System.out.println(" *       *");
	    System.out.println("  *     *");
	    System.out.println("   *   *");
	    System.out.println("     *\n");

	}

}
