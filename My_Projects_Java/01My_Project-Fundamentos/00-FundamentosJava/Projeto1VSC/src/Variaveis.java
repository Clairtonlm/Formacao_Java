public class Variaveis {
    public static void main(String[] args) {
        // VARIAVEIS DO TIPO INTEIRO
        //tipo nome byte tem 125 ate -127
        // tipo Short vai de -32768 ate 32767
        byte numeroPequeno = 125;
        short numeroShort = 32767;
        int numeroMaior = 2147483647;
        long numeroEnorme = 922337203685477000l;

        // VARIAVEIS DO TIPO FLUTUANTE
        //FLOAT
        float peso = 78.8f;
        double pi = 3.14784357837538; 

        // VARIAVEIS DO TIPO CARACTER
        char letra = 'C';

        //variavel do tipo String
        String nomeCliente = "Clairton Lima";

        // VARIAVEL DO TIPO BOOLEAN
        boolean estouComFome = true;
        boolean vouDormi = false;

        System.out.println("____________________________________________");
        System.out.println(numeroPequeno);
        System.out.println(numeroShort);
        System.out.println(numeroMaior);
        System.out.println(numeroEnorme);
        // VARIAVEIS DO TIPO FLUTUANTE
        System.out.println(peso);
        System.out.println(pi);
        // VARIAVEIS DO TIPO CARACTER
        System.out.println(letra);
        System.out.println(nomeCliente);
        
        

        
    }
        
}
